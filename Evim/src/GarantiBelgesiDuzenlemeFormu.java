    public void listeguncelle() {
        modelim.setRowCount(0);//mevcut tablonun icini sifirliyoruz herseferinde digerinin ustune eklenmesin diye
        veritabanibaglanti baglanti = new veritabanibaglanti();
        try {
            ResultSet res1 = baglanti.garantisisuresiazalanurunler();
            modelim.setColumnIdentifiers(sutun);
            while (res1.next()) {
                satirlar[0] = res1.getString("garantibelge_kayitno");
                satirlar[1] = res1.getString("garantibelge_urununturu");
                satirlar[2] = res1.getString("garantibelge_urununmarkasi");
                satirlar[3] = res1.getString("garantibelge_urununmodeli");
                satirlar[4] = res1.getString("garantibelge_urununadi");
                satirlar[5] = res1.getString("garantibelge_urununserino");
                satirlar[6] = res1.getString("garantibelge_urunungarantibitissuresi");
                satirlar[7] = res1.getString("garantibelge_urununfaturasi");

                modelim.addRow(satirlar);//yukarida tamımladigimiz satirlari rowsa ekledik 
            }

        } catch (Exception e) {
            // TODO: handle exception
        }

    }


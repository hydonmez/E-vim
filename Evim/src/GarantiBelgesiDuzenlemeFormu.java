    private void listele_butonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_listele_butonActionPerformed
        // TODO add your handling code here:

        int belgeno = Integer.parseInt(txt_belgeno.getText());
        veritabanibaglanti islem = new veritabanibaglanti();
        String sqlsorgu = "select*from garantibelge_tbl where garantibelge_kayitno=" + belgeno;
        ResultSet sonuc = null;
        sonuc = islem.verilisteleme(sqlsorgu);

        try {
            while (sonuc.next()) {

                txt_belgeno.setText(sonuc.getString("garantibelge_kayitno"));

                txt_marka.setText(sonuc.getString("garantibelge_urununmarkasi"));
                txt_model.setText(sonuc.getString("garantibelge_urununmodeli"));
                txt_urunadi.setText(sonuc.getString("garantibelge_urununadi"));
                txt_fatura.setText(sonuc.getString("garantibelge_urununfaturasi"));

                txt_serino.setText(sonuc.getString("garantibelge_urununserino"));
                cmbx_urunturu.setSelectedItem(sonuc.getString("garantibelge_urununturu"));

                String tarih = sonuc.getString("garantibelge_urunungarantibitissuresi");//tarihi jchoosera çekmek için ilk bir stringe çekiyoruz sonra alttarafta pars edip ekliyoruz
                Date date = new SimpleDateFormat("yyyy-MM-dd").parse(tarih);///yukarda devamı

                dc_garantibitistarihi.setDate(date);
                JOptionPane.showMessageDialog(null, "Kaydınız Basarıyla Listelenmistir!");

            }

            txt_belgeno.setEnabled(false);
            sil_buton.setEnabled(true);

            guncelle_buton.setEnabled(true);
            temizle_buton.setEnabled(true);
            buton_goster.setEnabled(true);

        } catch (Exception e) {
            JOptionPane.showConfirmDialog(null, "hatayakalandi", "kayıttablosu", JOptionPane.PLAIN_MESSAGE);
        }

        ////listeleme yapılacak 

    }//GEN-LAST:event_listele_butonActionPerformed

    private void guncelle_butonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guncelle_butonActionPerformed
        // TODO add your handling code here:

        veritabanibaglanti islem = new veritabanibaglanti();
        int belgeno = Integer.parseInt(txt_belgeno.getText());
        String marka = txt_marka.getText();
        String turu = cmbx_urunturu.getSelectedItem().toString();
        String model = txt_model.getText();
        String urunadi = txt_urunadi.getText();
        String fatura = txt_fatura.getText();
        fatura = fatura.replace("\\", "/");//yolu alırken bu satir yazılmazsa java kacıc olarak goruyor slashı siliyor o yüzden gerekli
        String serino = txt_serino.getText();

        SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");//tarih formatinda kaydetme icin gerekli 2 satir kod
        String garantibitistarihi = format.format(dc_garantibitistarihi.getDate());

        String sqlsorgu = "update garantibelge_tbl set garantibelge_kayitno=" + belgeno + "," + "garantibelge_urununturu='" + turu + "',garantibelge_urununmarkasi='" + marka + "',garantibelge_urununmodeli='" + model + "',garantibelge_urununadi='" + urunadi + "',garantibelge_urununserino='" + serino + "',garantibelge_urunungarantibitissuresi='" + garantibitistarihi + "',garantibelge_urununfaturasi='" + fatura + "'where garantibelge_kayitno=" + belgeno;
        islem.guncelleme(sqlsorgu);
        JOptionPane.showMessageDialog(null, "Kaydınızı Basarıyla Guncellediniz !");
        listeguncelle();

        temizle();


    }//GEN-LAST:event_guncelle_butonActionPerformed


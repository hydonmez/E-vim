    private void buton_guncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buton_guncelleActionPerformed
        // TODO add your handling code here:

        veritabanibaglanti islem = new veritabanibaglanti();
        int faturano = Integer.parseInt(txt_faturano.getText());
        String faturaturu = cmbx_faturaturu.getSelectedItem().toString();
        int faturamiktari = Integer.parseInt(txt_faturamiktari.getText());
        String faturadekont = txt_faturadekont.getText();
        faturadekont = faturadekont.replace("\\", "/");
        veritabanibaglanti baglanti = new veritabanibaglanti();
        SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
        String faturatarihi = format.format(dc_faturatarihi.getDate());
        SimpleDateFormat format1 = new SimpleDateFormat("yyyy-MM-dd");
        String faturaodemetarihi = format1.format(dc_faturaodemetarihi.getDate());

        String sqlsorgu = "update fatura_tbl set fatura_kayitno=" + faturano + "," + "fatura_miktari=" + faturamiktari + ",fatura_dekont='" + faturadekont + "',fatura_turu='" + faturaturu + "',fatura_tarihi='" + faturatarihi + "',fatura_odemetarihi='" + faturaodemetarihi + "'where fatura_kayitno=" + faturano;
        islem.guncelleme(sqlsorgu);
        JOptionPane.showMessageDialog(null, "Kaydınızı Basarıyla Guncellediniz !");
        tabloguncelle();

        temizle();
    }//GEN-LAST:event_buton_guncelleActionPerformed


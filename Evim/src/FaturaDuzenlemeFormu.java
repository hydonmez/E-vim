    private void buton_listeleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buton_listeleActionPerformed
        // TODO add your handling code here:
        int faturano = Integer.parseInt(txt_faturano.getText());
        veritabanibaglanti islem = new veritabanibaglanti();
        String sqlsorgu = "select*from fatura_tbl where fatura_kayitno=" + faturano;
        ResultSet sonuc = null;
        sonuc = islem.verilisteleme(sqlsorgu);

        try {
            while (sonuc.next()) {
                txt_faturano.setText(sonuc.getString("fatura_kayitno"));

                txt_faturamiktari.setText(sonuc.getString("fatura_miktari"));
                txt_faturadekont.setText(sonuc.getString("fatura_dekont"));
                cmbx_faturaturu.setSelectedItem(sonuc.getString("fatura_turu"));

                String faturatarihi = sonuc.getString("fatura_tarihi");//tarihi jchoosera çekmek için ilk bir stringe çekiyoruz sonra alttarafta pars edip ekliyoruz
                Date date = new SimpleDateFormat("yyyy-MM-dd").parse(faturatarihi);///yukarda devamı

                String odemetarihi = sonuc.getString("fatura_odemetarihi");//tarihi jchoosera çekmek için ilk bir stringe çekiyoruz sonra alttarafta pars edip ekliyoruz
                Date date1 = new SimpleDateFormat("yyyy-MM-dd").parse(odemetarihi);///yukarda devamı

                dc_faturatarihi.setDate(date);

                dc_faturaodemetarihi.setDate(date1);
                JOptionPane.showMessageDialog(null, "Kaydınız Basarıyla Listelenmistir!");

            }
            txt_faturano.setEnabled(false);
            buton_guncelle.setEnabled(true);
            buton_sil.setEnabled(true);
            button_goster.setEnabled(true);

        } catch (Exception e) {

        }


    }//GEN-LAST:event_buton_listeleActionPerformed

